import java.util.*;

public class Sample {
  public static void main(String[] args) {
    List<Integer> numbers = Arrays.asList(1, 2, 3, 4, 5, 6);

    int result = numbers.stream()
      .filter(e -> e % 2 == 0)
      .map(e -> e * 2)
      .reduce(0, (total, e) -> total + e);

    System.out.println(result);
  }
}

